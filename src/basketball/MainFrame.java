    private String getColorForRating(int value) {
        if (value >= 90) {
            return "#0a4a01";            // 90+ Dark Green
        } else if (value >= 80) {
            return "green";              // 80-89 Green
        } else if (value >= 70) {
            return "#04acb5";            // 70-79 Blue
        } else if (value >= 60) {
            return "orange";             // 60-69 Orange
        } else if (value >= 50) {
            return "#B8860B";            // 50-59 Yellow
        } else {
            return "red";                // Lower than 50 Red
        }
    }


    
